#include "3-calc.h"

/**
 * op_add - add
 * @a: a
 * @b: b
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substracts
 * @a: a
 * @b: b
 * Return: subtract
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies
 * @a: a
 * @b: b
 * Return: multiply
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides
 * @a: a
 * @b: b
 * Return: division
 */
int op_div(int a, int b)
{
	return ( a / b);
}

/**
 * op_mod - modulus
 * @a: a
 * @b: b
 * Return: modulus
 */
int op_mod(int a, int b)
{
	return (a % b);
}
